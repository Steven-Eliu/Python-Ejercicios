#Ingresar cuatro numero, calcular e informar la suma de los dos primeros y el producto del tercero y el cuarto
num1 = int(input('Ingrese el primer numero: '))
num2 = int(input('Ingrese el segundo numero: '))
num3 = int(input('Ingrese el tercer numero: '))
num4 = int(input('Ingrese el cuarto numero: '))

suma = (num1 + num2)
mult = (num3 * num4)

print('La suma de los dos primero numeros es: ')
print(suma)
print('La multiplicacion de los ultimos numero es: ')
print(mult)

