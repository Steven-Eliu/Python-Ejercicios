print('Tabla de multiplicar del 5: ')
#Algoritmo para mostrar la tabla de multiplicar del 5 con el while
x = 0 
while x <= 50:
    print(x)
    x = x + 5

print('Tabla de multiplicar del 5: ')
#Algoritmo para mostrar la tabla de multiplicar del 5 con el for
for n in range(0, 55, 5):
    print(n)
